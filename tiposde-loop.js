// exemplos de loop 

for (let i = 0; i < 10; i++) {
    console.log(i)
}
// o loop acima irá iniciar o contador do 0 até número 10


// ultilizando condicional e break no loop  

for (let i = 10; i > 0; i++) {
    if (i === 5) {
        break;
    }
}
console.log(i)

// o loop acima ira para o contador quando a contagem chegar no número 5